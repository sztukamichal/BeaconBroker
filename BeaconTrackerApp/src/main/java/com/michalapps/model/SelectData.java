package com.michalapps.model;

public class SelectData {
	
	private float id;
	private float title;
	
	public SelectData(float i, float t) {
		id = i;
		title = t;
	}
	
	public float getId() {
		return id;
	}
	public void setId(float id) {
		this.id = id;
	}
	public float getTitle() {
		return title;
	}
	public void setTitle(float title) {
		this.title = title;
	}
	
	

}
